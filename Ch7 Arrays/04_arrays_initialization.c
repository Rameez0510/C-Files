#include <stdio.h>

int main()
{
    int i[] = {5, 20, 50} ;
    printf("Value of i[0] is %d\n", i[0]);
    printf("Value of i[1] is %d\n", i[1]);
    printf("Value of i[2] is %d\n", i[2]);

    float f[] = {32.4, 65.659, 99.999};
    printf("Value of f[0] is %.2f\n", f[0]);
    printf("Value of f[1] is %.2f\n", f[1]);
    printf("Value of f[2] is %.2f\n", f[2]);
    return 0;
}