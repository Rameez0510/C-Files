// Area of rectangle
#include <stdio.h>

int main()
{
    int l, b ;
    printf("Enter the size of length \n");
    scanf("%d" , &l);

    printf("Enter the size of breadth \n");
    scanf("%d", &b);

    printf("The area of rectangle whose length is %dcm. and breadth is %dcm. is %dcm. sq. \n", l, b, l*b);
    return 0;
}
